num = int(input("Enter your number: "))
if 0<=num<=24:
    print("F Grade")
elif 24<num<=44:
    print("E Greade")
elif 44<num<=49:
    print("D Greade")
elif 49<num<=59:
    print("C Greade")
elif 59<num<=79:
    print("B Greade")
elif 79<num<=89:
    print("A Greade")
elif 89<num<=100:
    print("A+ Greade")
else:
    print("Invalid number")